class App2 {
    // 关系运算符
    public static void main(String[] args){
        System.out.println("===== 关系运算符 =====");
        int a = 10;
        int b = 3;
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("a >= b = " + (a >= b));
        System.out.println("a <= b = " + (a <= b));
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
    }
}