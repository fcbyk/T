// 数据类型
class App2 {

    /**
     * 基础数据类型
     * byte, short, int, long, float, double, char, boolean
     */
    public static void main(String[] args){
        // 整型
        byte a = 10;
        short b = 20;
        int c = 30;
        long d = 123456789123456789L;

        // 浮点型
        float e = 10.1F;
        double f = 20.3;

        // 字符型
        char g = 'a';

        // 布尔类型
        boolean h = true;
        boolean i = false;
    }

    /**
     * 引用数据类型
     * 字符串、类、接口、数组、枚举、注解、包装类等
     */
    public void dataType(){
        // 字符串
        String str = "hello";

        // 数组
        int[] arr1 = new int[10];
        int[] arr2 = {1, 2, 3, 4};
        int[][] arr3 = new int[2][3];
    }
}
