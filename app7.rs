// === Rust 切片 ===
//
// 对数组/vec 某段连续元素的引用，类型签名 &[T]。
// 不拥有数据，只是"借用"一段视图。
//
// Range 语法：
//   &arr[1..4]  → 索引 1、2、3（不含 4）
//   &arr[..3]   → 前 3 个
//   &arr[2..]   → 从索引 2 到结尾
//   &arr[..]    → 全部

fn main() {
    let arr = [10, 20, 30, 40, 50];

    let full: &[i32] = &arr;    // 完整切片
    let s1 = &arr[1..4];        // [20, 30, 40]
    let s2 = &arr[..3];         // [10, 20, 30]
    let s3 = &arr[2..];         // [30, 40, 50]

    println!("full = {full:?}, len = {}", full.len());
    println!("1..4 = {s1:?}");
    println!("..3  = {s2:?}");
    println!("2..  = {s3:?}");
    println!("..   = {:?}", &arr[..]);
}
