/**
正向先行断言：(?=条件)
作用：匹配一个位置，这个位置后面必须是指定内容
特点：只判断位置，不消耗字符（后面的内容不会被匹配）
 */

const print = (...args) => args.forEach(arg => console.log(arg))

const text1 = `
price: $100
price: $200
price: 300元
total: $50
apple123
banana456
`

/**
 * 基本用法：匹配特定模式前面的位置
 */
print(
    "$SAD$23".match(/\$(?=\d+)/),       // 匹配的$，后面必须跟着数字的
    text1.match(/\d+(?=元)/g),          // 匹配后面跟着 "元" 的数字（但不包含"元"）
)

/**
 * 理解「位置」概念：环视匹配的是空隙，不是字符
 * 空隙被确定后，前后的位置关系就固定了
 */
print(
    `hello world`.match(/.(?=l)/g), // 匹配前面是任意字符，后面是 l 的位置
    `hello world`.match(/.(?=o)/g), // 匹配前面是任意字符，后面是 o 的位置
)

/**
 * 实战1：给数字添加千分位逗号（经典应用）
 */
print(
    // 从右往左，每3位数字的前面插入逗号位置
    `1234567890`.replace(/(?=(\d{3})+$)/g, ','), 
    
    // 更精确的版本（避免在开头加逗号）
    // (?!条件) 负向先行断言
    // (?!^) 这个位置不能是开头
    `1234567890`.replace(/(?!^)(?=(\d{3})+$)/g, ','),
)