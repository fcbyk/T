// 变量与常量
class App1 {

    /**
     * 定义变量
     * 语法和c++差不多
     */
    public static void main(String[] args){

        int a = 16;
        double b = 10.1;
        Object c = new Object();

        int d,e,f,g;
        d = 100;
        e = 400;

        String h = "hello", i = "world";
    }

    // 常量使用 final 修饰符
    public void constVar(){
        final int A = 16;
        final double B = 10.1;
        final Object C = new Object();

        final String H = "hello", I = "world";
    }
}



